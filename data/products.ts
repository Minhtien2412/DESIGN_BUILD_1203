// Category types for construction industry
export type ProductCategory = 
  | 'villa'           // Biệt thự
  | 'interior'        // Nội thất
  | 'materials'       // Vật liệu xây dựng
  | 'architecture'    // Thiết kế kiến trúc
  | 'construction'    // Thi công
  | 'consultation'    // Tư vấn
  | 'furniture'       // Đồ nội thất
  | 'lighting'        // Thiết bị chiếu sáng
  | 'sanitary';       // Thiết bị vệ sinh

// Pricing type
export type PriceType = 'fixed' | 'contact';

// Seller/Company info
export interface Seller {
  id: string;
  name: string;
  type: 'individual' | 'company';
  logo?: any; // Image source
  rating?: number; // 0-5
  verified?: boolean;
  phone?: string;
  email?: string;
  address?: string;
  description?: string;
  yearsInBusiness?: number;
}

export type Product = {
  id: string;
  name: string;
  price: number; // VND - use 0 if priceType is 'contact'
  priceType?: PriceType; // 'fixed' (default) or 'contact'
  image: any; // require() static asset for expo-image
  description?: string;
  category?: ProductCategory | string;
  categoryId?: string;    // For navigation
  brand?: string;         // e.g. 'Panasonic', 'Sino', 'Toto'
  type?: string;          // e.g. 'Công tắc', 'Vòi', 'Đèn'
  discountPercent?: number; // optional discount (0-100)
  flashSale?: boolean;    // mark product in current flash sale
  stock?: number;         // optional stock count
  rating?: number;        // Product rating (0-5)
  reviewCount?: number;   // Number of reviews
  freeShipping?: boolean; // Free shipping available
  warranty?: string;      // Warranty info
  
  // Extended fields for product-metadata component
  origin?: string;        // Product origin/country
  tags?: string[];        // Product tags
  
  // Admin/Backend fields
  status?: 'DRAFT' | 'PENDING' | 'APPROVED' | 'REJECTED' | 'ACTIVE' | 'INACTIVE';
  isBestseller?: boolean;
  isNew?: boolean;
  sold?: number;
  createdBy?: string;
  createdAt?: string;
  updatedAt?: string;
  
  // Seller information (Shopee-style)
  seller?: Seller;        // Seller/company info
  soldCount?: number;     // Number sold
  location?: string;      // Product location
};

