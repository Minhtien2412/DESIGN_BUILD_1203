async function seed() {
  console.log("=== Worker Seed (Raw SQL) ===");
  console.log("Workers to seed:", WORKERS.length);

  // Check existing phones
  const existing = await p.$queryRawUnsafe(
    "SELECT phone FROM workers WHERE phone IS NOT NULL",
  );
  const existingPhones = new Set(existing.map((r) => r.phone));
  console.log("Existing workers:", existingPhones.size);

  const toInsert = WORKERS.filter((w) => !existingPhones.has(w.phone));
  console.log("New to insert:", toInsert.length);
  console.log("Skipped:", WORKERS.length - toInsert.length);

  if (toInsert.length === 0) {
    console.log("Nothing to insert.");
    await p.$disconnect();
    return;
  }

  let inserted = 0;
  let errors = 0;

  for (const w of toInsert) {
    try {
      await p.$executeRawUnsafe(
        `
        INSERT INTO workers (
          name, phone, worker_type, status, availability,
          experience_years, hourly_rate, daily_rate,
          location, district, city,
          description, specialties, certifications,
          rating, total_reviews, total_jobs, completed_jobs,
          is_verified, avatar, created_at, updated_at
        ) VALUES (
          $1, $2, $3::worker_type, $4::worker_status, $5,
          $6, $7, $8,
          $9, $10, $11,
          $12, $13::text[], $14::text[],
          $15, $16, $17, $18,
          $19, $20, NOW(), NOW()
        )
      `,
        w.name,
        w.phone,
        w.workerType,
        "active",
        "available",
        w.experienceYears,
        w.hourlyRate,
        w.dailyRate,
        w.location,
        w.district,
        w.city,
        w.description,
        w.specialties,
        w.certifications || [],
        w.rating,
        w.totalReviews,
        w.totalJobs,
        w.completedJobs,
        w.isVerified,
        `https://i.pravatar.cc/150?img=${10 + inserted}`,
      );
      inserted++;
    } catch (e) {
      errors++;
      console.error(
        `FAIL [${w.name}]:`,
        e.message.split("\n").slice(-2).join(" "),
      );
    }
  }

  console.log(`\nInserted: ${inserted}, Errors: ${errors}`);

  // Summary
  const total = await p.$queryRawUnsafe(
    "SELECT COUNT(*)::int as cnt FROM workers",
  );
  console.log("Total workers now:", total[0].cnt);

  const byType = await p.$queryRawUnsafe(`
    SELECT worker_type, COUNT(*)::int as cnt
    FROM workers GROUP BY worker_type ORDER BY cnt DESC
  `);
  console.log("\nBy type:");
  byType.forEach((t) => console.log(`  ${t.worker_type}: ${t.cnt}`));

  // Verify Prisma read
  try {
    const count = await p.worker.count();
    console.log("\nPrisma count:", count);
  } catch (e) {
    console.error("Prisma read error:", e.message);
  }

  await p.$disconnect();
}

seed().catch((e) => {
  console.error("Fatal:", e);
  process.exit(1);
});
