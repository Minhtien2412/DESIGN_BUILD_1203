/*
  Generate data/localVideos.generated.ts from assets/videos/**
  - Groups by subfolder as category slug (folder name)
  - Root-level files fall under category 'reels'
  - Emits require() calls so Metro bundles the assets
*/
const fs = require('fs');
const path = require('path');

const ROOT = process.cwd();
const VIDEOS_DIR = path.join(ROOT, 'assets', 'videos');
const OUT_FILE = path.join(ROOT, 'data', 'localVideos.generated.ts');
const MAX_MB = Number(process.env.VIDEOS_MAX_MB || 15); // default 15MB threshold

function toSlug(name) {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9\-\s_]/gi, '')
    .replace(/[\s_]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
}

function toTitle(name) {
  const base = name.replace(/\.[^.]+$/, '')
    .replace(/[-_]+/g, ' ')
    .trim();
  return base.charAt(0).toUpperCase() + base.slice(1);
}

function walk(dir, baseCategory = null) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  let list = [];
  for (const e of entries) {
    if (e.isDirectory()) {
      const cat = toSlug(e.name);
      list = list.concat(walk(path.join(dir, e.name), cat));
    } else if (e.isFile()) {
      const ext = path.extname(e.name).toLowerCase();
      if (ext === '.mp4' || ext === '.m3u8') {
        const full = path.join(dir, e.name);
        const stat = fs.statSync(full);
        const sizeMB = stat.size / (1024 * 1024);
        if (sizeMB > MAX_MB) {
          console.warn(`[videos:generate] Skipping large file (> ${MAX_MB}MB): ${full} (${sizeMB.toFixed(1)}MB)`);
          continue;
        }
        const rel = path
          .relative(path.join(ROOT, 'data'), path.join(dir, e.name))
          .replace(/\\/g, '/'); // from data/ folder to asset file
        const cat = baseCategory || 'reels';
        const id = `local:${cat}:${toSlug(e.name.replace(/\.[^.]+$/, ''))}`;
  list.push({ id, title: toTitle(e.name), requirePath: rel, category: cat, type: 'vod' });
      }
    }
  }
  return list;
}

function main() {
  if (!fs.existsSync(VIDEOS_DIR)) {
    console.error('Missing assets/videos directory');
    process.exit(1);
  }
  const items = walk(VIDEOS_DIR);
  items.sort((a, b) => a.title.localeCompare(b.title));

  const header = `// AUTO-GENERATED by scripts/generate-video-manifest.cjs\n` +
    `// Do not edit manually. Re-run the script after adding/removing videos.\n` +
    `// Note: Files larger than ${MAX_MB}MB are skipped. Override with env VIDEOS_MAX_MB.\n` +
    `import type { VideoItem } from './videos';\n\n`;

  const bodyItems = items.map((it) => {
    return `  { id: '${it.id}', title: '${it.title.replace(/'/g, "\\'")}', url: require('${it.requirePath}'), category: '${it.category}', type: '${it.type}' }`;
  }).join(',\n');

  const content = header + `export const GENERATED_VIDEOS: VideoItem[] = [\n${bodyItems}\n];\n`;
  fs.writeFileSync(OUT_FILE, content, 'utf8');
  console.log(`Wrote ${items.length} items to ${path.relative(ROOT, OUT_FILE)}`);
}

main();
